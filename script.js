// Function to synchronize the initiative-header height with the 3 lines of the scrollable header
function syncHeaderHeight() {
    const initiativeHeader = document.querySelector('.fixed-table .initiative-header');
    const scrollableHeaderRows = document.querySelectorAll('.scrollable-table thead tr');
    
    if (initiativeHeader && scrollableHeaderRows.length >= 3) {
 
        let totalHeight = 0;
        for (let i = 0; i < 3; i++) {
            totalHeight += scrollableHeaderRows[i].offsetHeight;
        }
        
        if (totalHeight > 0) {
            initiativeHeader.style.height = `${totalHeight - 1}px`;
        }
    }
}

// Function to synchronize row heights between tables
function syncRowHeights() {
    const fixedRows = document.querySelectorAll('.fixed-table tbody tr');
    const scrollableRows = document.querySelectorAll('.scrollable-table tbody tr');
    
    const minRows = Math.min(fixedRows.length, scrollableRows.length);
    
    for (let i = 0; i < minRows; i++) {
        const fixedRow = fixedRows[i];
        const scrollableRow = scrollableRows[i];
        
        const computedStyle = window.getComputedStyle(fixedRow);
        const fixedHeight = fixedRow.offsetHeight;
        
        if (fixedHeight > 0) {
            scrollableRow.style.height = `${fixedHeight}px`;
        }
    }
}

// Function to update cell size
function updateCellSize(size) {
    const cells = document.querySelectorAll('.scrollable-table .quarter-header:not(.month-header)');
    size = size * 2;
    cells.forEach(cell => {
        cell.style.minHeight = `${size}px`;
        cell.style.paddingRight = `${size}px`;
        cell.style.paddingLeft = `${size}px`;
    });
    
    setTimeout(() => {
        syncHeaderHeight(); 
        syncRowHeights(); 
    }, 50);
}

// Function to redraw all dependency arrows
function redrawAllArrows() {
    // Remove old arrows
    const oldArrows = document.querySelectorAll('.dependency-arrow');
    oldArrows.forEach(arrow => arrow.remove());
    
    // Small delay to ensure elements are rendered
    setTimeout(() => {
        // Recreate arrows
        dataGantt.forEach(task => {
            if (task.dependencies && task.dependencies.length > 0) {
                task.dependencies.forEach(dependency => {
                    const startElement = document.querySelector(`[data-task-id="${dependency.predecessorId}"]`);
                    const endElement = document.querySelector(`[data-task-id="${dependency.interdependentId}"]`);
                    
                    // Check if elements exist and are visible
                    if (startElement && endElement && 
                        startElement.offsetParent !== null && 
                        endElement.offsetParent !== null) {
                        // Create dependency arrow
                        createDependencyArrow(
                            startElement, 
                            endElement, 
                            dependency.relation,
                            dependency.color
                        );
                    }
                });
            }
        });
        
        // Synchronize heights after redrawing arrows
        syncHeaderHeight(); // Synchronize header height
        syncRowHeights(); // Synchronize row heights
    }, 300); // Small delay of 300ms
}

// Function to get all quarters with data in the first fiscal year
function getFirstYearQuartersWithData() {
    const fiscalYears = getAllFiscalYears();
    if (fiscalYears.length === 0) return [];

    const firstYear = fiscalYears[0];
    const quarters = getAllQuarters();
    const quartersWithData = new Set();

    // Check if there is data in each quarter
    dataGantt.forEach(item => {
        if (item.fiscalYear === firstYear) {
            quartersWithData.add(item.quarter);
        }
    });

    // Determine which quarters should be visible
    const visibleQuarters = [];
    let foundFirstQuarter = false;

    quarters.forEach(quarter => {
        if (quartersWithData.has(quarter)) {
            foundFirstQuarter = true;
            visibleQuarters.push(quarter);
        } else if (foundFirstQuarter) {
            // If we've already found a quarter with data, we include the subsequent ones
            visibleQuarters.push(quarter);
        }
    });

    return visibleQuarters;
}

// Function to update the visibility of the first months
function updateFirstMonthsVisibility(showFirstMonths) {
    const firstYearVisibleQuarters = getFirstYearQuartersWithData();
    const fiscalYears = getAllFiscalYears();
    const firstYear = fiscalYears[0];
    
    const totalQuarters = getAllQuarters().length;
    const visibleQuarters = firstYearVisibleQuarters.length;
    const hiddenQuarters = totalQuarters - visibleQuarters;
    
    const fiscalYearHeaders = document.querySelectorAll('.scrollable-table .fiscal-year-header');
    fiscalYearHeaders.forEach((header, index) => {
        if (index === 0) { // First fiscal year
            // Store the original colspan if it hasn't been stored yet
            if (!header.dataset.originalColspan) {
                // The original colspan is always 12 (4 quarters * 3 months)
                header.dataset.originalColspan = '12';
            }

            if (showFirstMonths) {
                header.setAttribute('colspan', header.dataset.originalColspan);
            } else {
                const newColspan = visibleQuarters * 3;
                header.setAttribute('colspan', newColspan.toString());
            }
        }
    });
}

// Function to remove duplicate sub-groups and adjust rowspan
function removeDuplicateSubGroups() {
    const subGroups = document.querySelectorAll('.fixed-table .sub-group, .scrollable-table .sub-group:not(.hidden-initiative)');
    const subGroupMap = new Map();

    // Group sub-groups by text
    subGroups.forEach(subGroup => {
        const text = subGroup.textContent.trim();
        if (!subGroupMap.has(text)) {
            subGroupMap.set(text, []);
        }
        subGroupMap.get(text).push(subGroup);
    });

    // Process each group of identical sub-groups
    subGroupMap.forEach((group, text) => {
        if (group.length > 1) {
            const firstElement = group[0];
            const rowspan = group.length;
            
            // Adjust the rowspan of the first element
            firstElement.setAttribute('rowspan', rowspan);
            
            // Remove duplicate elements
            for (let i = 1; i < group.length; i++) {
                const row = group[i].closest('tr');
                if (row) {
                    group[i].remove();
                }
            }
        }
    });
}


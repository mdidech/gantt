// Function to initialize the floating scrollbar
function initializeFloatingScrollbar() {
   
    if (window.floatingScrollbarInitialized) {
        return;
    }
    
    let scrollableColumns = document.querySelector('.scrollable-wrapper');
    
    if (!scrollableColumns) {
        scrollableColumns = document.querySelector('.scrollable-columns');
    }

    if (!scrollableColumns) {
        scrollableColumns = document.querySelector('#scrollableTable');
    }
    
    const floatingScrollbar = document.getElementById('floatingScrollbar');
    const scrollTrack = document.getElementById('scrollTrack');
    const scrollThumb = document.getElementById('scrollThumb');
    const scrollLeftBtn = document.getElementById('scrollLeft');
    const scrollRightBtn = document.getElementById('scrollRight');

    if (!scrollableColumns || !floatingScrollbar) {
        console.log('Elements not found:', { 
            scrollableColumns: !!scrollableColumns, 
            floatingScrollbar: !!floatingScrollbar,
            scrollableColumnsElement: scrollableColumns
        });
        return;
    }

    console.log('Initializing floating scrollbar');
    console.log('scrollableColumns:', scrollableColumns);
    console.log('scrollWidth:', scrollableColumns.scrollWidth, 'clientWidth:', scrollableColumns.clientWidth);

    let isDragging = false;
    let startX = 0;
    let isUpdatingScroll = false; 

    function updateScrollThumb() {
        if (isUpdatingScroll) return; 
        
        const scrollWidth = scrollableColumns.scrollWidth;
        const clientWidth = scrollableColumns.clientWidth;
        const maxScroll = Math.max(0, scrollWidth - clientWidth);
        
        console.log('updateScrollThumb:', { scrollWidth, clientWidth, maxScroll });
        
        if (maxScroll > 0 && scrollTrack && scrollThumb) {
            const trackWidth = scrollTrack.offsetWidth;
            const thumbWidth = Math.max(20, (clientWidth / scrollWidth) * trackWidth);
            scrollThumb.style.width = thumbWidth + 'px';
            
            const scrollPercent = Math.max(0, Math.min(1, scrollableColumns.scrollLeft / maxScroll));
            const maxThumbLeft = trackWidth - thumbWidth;
            const thumbLeft = scrollPercent * maxThumbLeft;
            scrollThumb.style.left = thumbLeft + 'px';
            
            floatingScrollbar.style.display = 'block';
            if (scrollLeftBtn) scrollLeftBtn.style.display = 'flex';
            if (scrollRightBtn) scrollRightBtn.style.display = 'flex';
            
            console.log('Scroll active:', { thumbWidth, scrollPercent, maxThumbLeft, thumbLeft });
        } else {
            floatingScrollbar.style.display = 'none';
            if (scrollLeftBtn) scrollLeftBtn.style.display = 'none';
            if (scrollRightBtn) scrollRightBtn.style.display = 'none';
            console.log('No scroll needed');
        }
    }

    function updateTableScroll(thumbLeft) {
        if (!scrollTrack || !scrollThumb) return;
        
        isUpdatingScroll = true; // Prevent loops
        
        const trackWidth = scrollTrack.offsetWidth;
        const thumbWidth = scrollThumb.offsetWidth;
        const maxThumbLeft = trackWidth - thumbWidth;
        const scrollPercent = Math.max(0, Math.min(1, thumbLeft / maxThumbLeft));
        
        const scrollWidth = scrollableColumns.scrollWidth;
        const clientWidth = scrollableColumns.clientWidth;
        const maxScroll = Math.max(0, scrollWidth - clientWidth);
        
        const newScrollLeft = scrollPercent * maxScroll;
        scrollableColumns.scrollLeft = newScrollLeft;
        
        console.log('updateTableScroll:', { thumbLeft, scrollPercent, newScrollLeft });
        
        // Reset flag after a small delay
        setTimeout(() => {
            isUpdatingScroll = false;
        }, 10);
    }

    
    if (scrollThumb) {
        scrollThumb.addEventListener('mousedown', (e) => {
            isDragging = true;
            startX = e.clientX - scrollThumb.offsetLeft;
            scrollThumb.style.cursor = 'grabbing';
            console.log('Starting drag:', { startX });
            e.preventDefault();
        });
    }

    document.addEventListener('mousemove', (e) => {
        if (!isDragging || !scrollThumb) return;
        
        const newLeft = e.clientX - startX;
        const maxLeft = scrollTrack.offsetWidth - scrollThumb.offsetWidth;
        const clampedLeft = Math.max(0, Math.min(newLeft, maxLeft));
        
        scrollThumb.style.left = clampedLeft + 'px';
        updateTableScroll(clampedLeft);
    });

    document.addEventListener('mouseup', () => {
        if (isDragging && scrollThumb) {
            isDragging = false;
            scrollThumb.style.cursor = 'grab';
            console.log('Ending drag');
        }
    });

    if (scrollTrack) {
        scrollTrack.addEventListener('click', (e) => {
            if (!scrollThumb) return;
            
            const rect = scrollTrack.getBoundingClientRect();
            const clickX = e.clientX - rect.left;
            const thumbCenter = scrollThumb.offsetLeft + scrollThumb.offsetWidth / 2;
            
            console.log('Click on track:', { clickX, thumbCenter });
            
            const scrollAmount = scrollableColumns.clientWidth * 0.8;
            if (clickX < thumbCenter) {
                // Scroll left
                scrollableColumns.scrollLeft = Math.max(0, scrollableColumns.scrollLeft - scrollAmount);
            } else {
                // Scroll right
                const maxScroll = scrollableColumns.scrollWidth - scrollableColumns.clientWidth;
                scrollableColumns.scrollLeft = Math.min(maxScroll, scrollableColumns.scrollLeft + scrollAmount);
            }
            
            updateScrollThumb();
        });
    }

    if (scrollLeftBtn) {
        scrollLeftBtn.addEventListener('click', () => {
            console.log('Left button clicked');
            const scrollAmount = scrollableColumns.clientWidth * 0.5;
            scrollableColumns.scrollLeft = Math.max(0, scrollableColumns.scrollLeft - scrollAmount);
            updateScrollThumb();
            floatingScrollbar.classList.add('scrolling');
            setTimeout(() => floatingScrollbar.classList.remove('scrolling'), 300);
        });
    }

    if (scrollRightBtn) {
        scrollRightBtn.addEventListener('click', () => {
            console.log('Right button clicked');
            const scrollAmount = scrollableColumns.clientWidth * 0.5;
            const maxScroll = scrollableColumns.scrollWidth - scrollableColumns.clientWidth;
            scrollableColumns.scrollLeft = Math.min(maxScroll, scrollableColumns.scrollLeft + scrollAmount);
            updateScrollThumb();
            floatingScrollbar.classList.add('scrolling');
            setTimeout(() => floatingScrollbar.classList.remove('scrolling'), 300);
        });
    }

    let scrollTimeout;
    scrollableColumns.addEventListener('scroll', () => {
        if (scrollTimeout) clearTimeout(scrollTimeout);
        scrollTimeout = setTimeout(() => {
            console.log('Table scroll detected:', scrollableColumns.scrollLeft);
            updateScrollThumb();
        }, 16); 
    });

    let resizeTimeout;
    window.addEventListener('resize', () => {
        if (resizeTimeout) clearTimeout(resizeTimeout);
        resizeTimeout = setTimeout(updateScrollThumb, 100);
    });

    document.addEventListener('keydown', (e) => {
        if (e.key === 'ArrowLeft' || e.key === 'ArrowRight') {
            const isLeft = e.key === 'ArrowLeft';
            const scrollAmount = scrollableColumns.clientWidth * 0.3;
            
            if (isLeft) {
                scrollableColumns.scrollLeft = Math.max(0, scrollableColumns.scrollLeft - scrollAmount);
            } else {
                const maxScroll = scrollableColumns.scrollWidth - scrollableColumns.clientWidth;
                scrollableColumns.scrollLeft = Math.min(maxScroll, scrollableColumns.scrollLeft + scrollAmount);
            }
            
            floatingScrollbar.classList.add('scrolling');
            setTimeout(() => floatingScrollbar.classList.remove('scrolling'), 300);
            updateScrollThumb();
        }
    });

    scrollableColumns.addEventListener('wheel', (e) => {
        if (e.shiftKey) {
            e.preventDefault();
            const delta = e.deltaY;
            const maxScroll = scrollableColumns.scrollWidth - scrollableColumns.clientWidth;
            
            scrollableColumns.scrollLeft = Math.max(0, Math.min(maxScroll, scrollableColumns.scrollLeft + delta));
            
            floatingScrollbar.classList.add('scrolling');
            setTimeout(() => floatingScrollbar.classList.remove('scrolling'), 300);
            updateScrollThumb();
        }
    });

    updateScrollThumb();

    const observer = new MutationObserver(() => {
        if (scrollTimeout) clearTimeout(scrollTimeout);
        scrollTimeout = setTimeout(updateScrollThumb, 100);
    });

    observer.observe(scrollableColumns, {
        childList: true,
        subtree: true,
        attributes: true
    });

    window.floatingScrollbarInitialized = true;
}


// Function to get the month number from the abbreviated month name
function getMonthNumber(monthName) {
    const months = {
        'Jan': 1, 'Feb': 2, 'Mar': 3, 'Apr': 4, 'May': 5, 'Jun': 6,
        'Jul': 7, 'Aug': 8, 'Sep': 9, 'Oct': 10, 'Nov': 11, 'Dec': 12
    };
    return months[monthName];
}

// Function to get the abbreviated month name in English
function getMonthName(monthNumber) {
    const months = [
        'Jan', 'Feb', 'Mar', 'Apr', 'May', 'Jun',
        'Jul', 'Aug', 'Sep', 'Oct', 'Nov', 'Dec'
    ];
    return months[monthNumber - 1];
}

// Function to get all months of a quarter
function getMonthsForQuarter(quarter) {
    const quarterNumber = parseInt(quarter.replace('Q', ''));

    const startMonth = ((quarterNumber - 1) * 3 + 4) % 12;
    
    const actualStartMonth = startMonth === 0 ? 12 : startMonth;
    return Array.from({length: 3}, (_, i) => {
        const monthNumber = (actualStartMonth + i - 1) % 12 + 1;
        return getMonthName(monthNumber);
    });
}

// Function to create a dependency arrow
function createDependencyArrow(startElement, endElement, relation, color) {
    const container = document.querySelector('.scrollable-columns');
    const fixedTable = document.querySelector('.fixed-table');
    const arrow = document.createElement('div');
    arrow.className = 'dependency-arrow';
    

    if (color) {
        arrow.classList.add(`dependency-${color.toLowerCase().trim()}`);
    }
    

    const startRect = startElement.getBoundingClientRect();
    const endRect = endElement.getBoundingClientRect();
    const containerRect = container.getBoundingClientRect();
    const fixedTableRect = fixedTable.getBoundingClientRect();
    

    const fixedTableWidth = fixedTableRect.width;
    

    const startX = (startRect.left - containerRect.left + startRect.width / 2) + 10;
    const startY = startRect.top - containerRect.top + startRect.height / 2;
    const endX = endRect.left - containerRect.left + endRect.width / 2;
    const endY = endRect.top - containerRect.top + endRect.height / 2;
    
    // Adjust the Y position to align with the center of the icons
    const iconOffset = 3; 
    const recuo = Math.min(50, Math.abs(endX - startX) / 2); 
    const finalOffset = 14; 
    
    // Create horizontal line
    const horizontalLine1 = document.createElement('div');
    horizontalLine1.className = 'dependency-line horizontal';
    horizontalLine1.style.left = `${startX}px`;
    horizontalLine1.style.top = `${startY + iconOffset}px`;
    horizontalLine1.style.width = `${Math.max(0, Math.abs(endX - startX) - recuo)}px`; 
    arrow.appendChild(horizontalLine1);
    
    // Create vertical line if necessary  
    if (Math.abs(endY - startY) > 0) {
        const verticalLine = document.createElement('div');
        verticalLine.className = 'dependency-line vertical';
        verticalLine.style.left = `${endX - recuo}px`;  
        verticalLine.style.top = `${Math.min(startY + iconOffset, endY + iconOffset)}px`;
        verticalLine.style.height = `${Math.abs(endY - startY)}px`;
        arrow.appendChild(verticalLine);
    }
    
    const horizontalLine2 = document.createElement('div');
    horizontalLine2.className = 'dependency-line horizontal';
    horizontalLine2.style.left = `${endX - recuo}px`; 
    horizontalLine2.style.top = `${endY + iconOffset}px`;
    horizontalLine2.style.width = `${Math.max(0, recuo - finalOffset)}px`; 
    arrow.appendChild(horizontalLine2);
    

    const arrowHead = document.createElement('div');
    arrowHead.className = 'dependency-arrow-head';
    arrowHead.style.left = `${endX - finalOffset}px`; 
    arrowHead.style.top = `${endY + iconOffset}px`;
    arrow.appendChild(arrowHead);
    
    container.appendChild(arrow);
}

